export const regs = {
  visa: /^4[0-9]{0,12}(?:[0-9]{0,3})?$/,
  american_express: /^3[47][0-9]{0,13}$/,
  diners_club: /^3(?:0[0-5]|[68][0-9])[0-9]{0,11}$/,
  master_card:
    /^(5[1-5][0-9]{0,14}|2(22[1-9][0-9]{0,12}|2[3-9][0-9]{13}|[3-6][0-9]{14}|7[0-1][0-9]{13}|720[0-9]{12}))$/,
  discover:
    /^65[4-9][0-9]{0,13}|64[4-9][0-9]{0,13}|6011[0-9]{0,12}|(622(?:12[6-9]|1[3-9][0-9]|[2-8][0-9][0-9]|9[01][0-9]|92[0-5])[0-9]{0,10})$/,
  maestro: /^(5018|5020|5038|6304|6759|6761|6763)[0-9]{0,15}$/,
  mir: /^(220[0-4])[0-9]+$/,
};

export default class CardFormat {
  checkFormat(number) {
    for (let form in regs) {
      if (regs[form].test(number)) {
        return form;
      }
    }
  }
}
